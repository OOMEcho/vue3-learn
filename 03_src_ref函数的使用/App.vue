<template>
  <h1>我是app组件</h1>
  <h1>我叫{{ name }}, {{ age }}岁</h1>
  <h3>职位:{{ job.type }}</h3>
  <h3>薪水:{{ job.salary }}</h3>
  <button @click="changeInfo">修改人的信息</button>
</template>

<script>
import { ref } from 'vue';
export default {
  name: 'App',
  setup(){
    //ref实现响应式(基本类型)也是采用Object.definedProperty()来实现的 getter和setter
    let name = ref('张三'); //ref引用对象
    let age = ref(18);
    //ref实现响应式(对象类型)也是采用Proxy来实现
    let job = ref({
      type: '前端工程师',
      salary: '30k'
    });

    function changeInfo(){
      name.value = '李四';
      age.value = 42;
      job.value.type = 'UI工程师';
      job.value.salary = '40k';
    }

    return {
      name,
      age,
      job,
      changeInfo
    }
  }
}
</script>

